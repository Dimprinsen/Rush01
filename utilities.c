int	ft_atoi(char *str)
{
	int	nbr;
	int	i;

	nbr = 0;
	i = 0;
	while (str[i] >= '0' && str[i] <= '4')
	{
		nbr = nbr * 10 + str[i] - 48;
		i++;
	}
	return (nbr);
}
